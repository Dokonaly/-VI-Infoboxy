package Vzorka;

public class Infobox_person {
	
	private String name;
	private String image;
	private String image_size;
	private String birth_date;
	private String birth_place;
	private String death_date;
	private String death_place;
	private String occupation;
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the image
	 */
	public String getImage() {
		return image;
	}
	/**
	 * @param image the image to set
	 */
	public void setImage(String image) {
		this.image = image;
	}
	/**
	 * @return the image_size
	 */
	public String getImage_size() {
		return image_size;
	}
	/**
	 * @param image_size the image_size to set
	 */
	public void setImage_size(String image_size) {
		this.image_size = image_size;
	}
	/**
	 * @return the birth_date
	 */
	public String getBirth_date() {
		return birth_date;
	}
	/**
	 * @param birth_date the birth_date to set
	 */
	public void setBirth_date(String birth_date) {
		this.birth_date = birth_date;
	}
	/**
	 * @return the birth_place
	 */
	public String getBirth_place() {
		return birth_place;
	}
	/**
	 * @param birth_place the birth_place to set
	 */
	public void setBirth_place(String birth_place) {
		this.birth_place = birth_place;
	}
	/**
	 * @return the death_date
	 */
	public String getDeath_date() {
		return death_date;
	}
	/**
	 * @param death_date the death_date to set
	 */
	public void setDeath_date(String death_date) {
		this.death_date = death_date;
	}
	/**
	 * @return the death_place
	 */
	public String getDeath_place() {
		return death_place;
	}
	/**
	 * @param death_place the death_place to set
	 */
	public void setDeath_place(String death_place) {
		this.death_place = death_place;
	}
	/**
	 * @return the occupation
	 */
	public String getOccupation() {
		return occupation;
	}
	/**
	 * @param occupation the occupation to set
	 */
	public void setOccupation(String occupation) {
		this.occupation = occupation;
	}
	
}
